/**
 * Repräsentiert die Steuerungseingaben des Spielers über die Tastatur.
 */
class Keyboard {
    /** @type {boolean} Gibt an, ob die linke Pfeiltaste gedrückt ist. */
    LEFT = false;
    
    /** @type {boolean} Gibt an, ob die rechte Pfeiltaste gedrückt ist. */
    RIGHT = false;
    
    /** @type {boolean} Gibt an, ob die obere Pfeiltaste (Springen) gedrückt ist. */
    UP = false;
    
    /** @type {boolean} Gibt an, ob die untere Pfeiltaste gedrückt ist. */
    DOWN = false;
    
    /** @type {boolean} Gibt an, ob die Leertaste (Aktionen) gedrückt ist. */
    SPACE = false;
    
    /** @type {boolean} Gibt an, ob die Taste 'D' (Flasche werfen) gedrückt ist. */
    D = false;
}
